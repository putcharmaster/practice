int	is_power_of_2(int n)
{
	return (n > 0 && !(n & n-1));
}

int	ispowerof2(int n)
{
	if (n == 0)
		return (0);
	while (n % 2 == 0)
		n /= 2;
	if (n == 1)
		return (1);
	else
		return (0);
}
