#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dup;

	dup = (char *)malloc(ft_strlen(src) + 1);
	if (dup == 0)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
